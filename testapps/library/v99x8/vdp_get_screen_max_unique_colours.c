#include <ez80.h>
#include <stdbool.h>
#include <stdlib.h>
#include <v99x8.h>

uint16_t vdp_get_screen_max_unique_colours(void) {
  switch (vdp_current_mode) {
  case 1:
  case 2:
  case 3:
  case 4:
  case 6:
  case 0x80 | 0x10 | 0x05:
  case 0x80 | 0x10 | 0x06:
  case 0x80 | 0x10 | 0x07:
  case 0x80 | 0x10 | 0x08:
  case 0x80 | 0x10 | 0x09:
  case 0x80 | 0x10 | 0x0A:
  case 0x80 | 0x10 | 0x0B:
  case 0x80 | 0x10 | 0x0C:
    return 16;

  case 5:
    return 4;

  case 7:
  case 0x80 | 0x01:
  case 0x80 | 0x02:
  case 0x80 | 0x03:
  case 0x80 | 0x04:
  case 0x80 | 0x05:
  case 0x80 | 0x06:
  case 0x80 | 0x07:
  case 0x80 | 0x08:
  case 0x80 | 0x09:
  case 0x80 | 0x0A:
  case 0x80 | 0x0B:
  case 0x80 | 0x0C:
    return 256;

  default:
    return 0;
  }
}
